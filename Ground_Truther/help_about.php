		<h3>Not sure what to do?</h3>
		This tool was created to help with collecting ground truthing data for scientists who work with satellite imagery.
		Anyone can contribute to this project. The first step is to register as a new user. Please give us your contact details 
		if you don't mind being contacted solemnly regarding the data you submit.
		Once registered, you log in and can start contributing your observations. 
		It is very easy:
		<li> enter the coordinates of the point where you observed a disturbance. You can just draw a marker on the interactive map 
		and the coordinates of the last marker drawn will be used 
		<li> select the type of landscape and disturbance you observed available from the dropdown menus
		<li> enter the date of your observation
		<li> type in an optional comment
		<li> upload an optional photo of the place.
		<br>
		You can also view existing points by clicking the link to Results Map at the bottom of the page.
		Select what kind of points you'd like to see and click the Query button.
